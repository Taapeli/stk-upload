class CypherObject():
    """ For linking existing objects from Gramps data. """

    link_citation = """
MATCH (x)          WHERE x.handle = $handle
MATCH (c:Citation) WHERE c.handle = $c_handle
MERGE (x) -[:CITATION]-> (c)
RETURN c"""
